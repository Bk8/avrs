function t = calcTiempoDirecta(fi, fr)
% CALCTIEMPODIRECTA Calcula el tiempo en segundos que está la
% fuente-imágene pasada por parámetro de la fuente directa
% Argumentos de entrada:
% fi: fuente-imágen de la cual se quiere calcular el tiempo relativo
% fr: fuente real
% Argumentos de salida:
% t: tiempo en segundos
%
% Autor: Fabián C. Tommasini
% Creado: 08/2007

vSonido = 343;  % en m/s
dist = get(fr, 'DistReceptor');
t = (fi.DistReceptor - dist) / vSonido;
